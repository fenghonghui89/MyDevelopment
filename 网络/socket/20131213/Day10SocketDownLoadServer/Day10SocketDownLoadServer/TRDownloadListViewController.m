- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return self.filePathAndLength_array.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    NSString* filePathAndLength = self.filePathAndLength_array[indexPath.row];
    NSArray* array = [filePathAndLength componentsSeparatedByString:@"&&"];
    NSString* filePath = [array objectAtIndex:0];
    NSString* fileLength = [array objectAtIndex:1];
    NSString* fileName = [filePath lastPathComponent];
    
    cell.textLabel.text = fileName;
    cell.detailTextLabel.text = fileLength;
    
    return cell;
}

